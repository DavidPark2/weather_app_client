module.exports.convertDay = function(timestamp) {
	var d = new Date(timestamp * 1000),	// Convert the passed timestamp to milliseconds
		yyyy = d.getFullYear(),
		mm = ('0' + (d.getMonth() + 1)).slice(-2),	// Months are zero based. Add leading 0.
		dd = ('0' + d.getDate()).slice(-2),			// Add leading 0.
		hh = d.getHours(),
		day = d.getDay(),
		h = hh,
		min = ('0' + d.getMinutes()).slice(-2),		// Add leading 0.
		ampm = 'AM',
		days = '',
		time;
		
	if (hh > 12) {
		h = hh - 12;
		ampm = 'PM';
	} else if (hh === 12) {
		h = 12;
		ampm = 'PM';
	} else if (hh == 0) {
		h = 12;
	}

	if (day === 1) {
		days = 'Monday'
	} else if (day === 2) {
		days = 'Tuesday'
	} else if (day === 3) {
		days = 'Wednesday'
	} else if (day === 4) {
		days = 'Thursday'
	} else if (day === 5) {
		days = 'Friday'
	} else if (day === 6) {
		days = 'Saturday'
	} else {
		days = 'Sunday'
	}
	
	time = days;
		
	return time;
}

